package com.cubeia.poker.handhistory.api;

import java.io.Serializable;

public abstract class HandHistoryEvent implements Serializable {

    private static final long serialVersionUID = 7429903140165014580L;
	
    private final String type;
    private long time;

    protected HandHistoryEvent() {
        this.type = getClass().getSimpleName();
        this.time = System.currentTimeMillis();
    }

    public long getTime() {
        return time;
    }

    public void setTime(long time) {
        this.time = time;
    }

    public String getType() {
        return type;
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + (int) (time ^ (time >>> 32));
        result = prime * result + ((type == null) ? 0 : type.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (getClass() != obj.getClass())
            return false;
        HandHistoryEvent other = (HandHistoryEvent) obj;
        if (time != other.time)
            return false;
        if (type == null) {
            if (other.type != null)
                return false;
        } else if (!type.equals(other.type))
            return false;
        return true;
    }

    @Override
    public String toString() {
        return "HandHistoryEvent [type=" + type + ", time=" + time + "]";
    }
}
